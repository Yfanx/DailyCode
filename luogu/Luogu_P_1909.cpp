#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define i64 long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  int min = 0x7fffffff;
  int count = 3;
  while (count--) {
    int a, b;
    cin >> a >> b;
    int temp = n / a;
    if (n % a)
      temp++;
    temp *= b;
    if (temp < min) {
      min = temp;
    }
  }
  cout << min;
  return 0;
}