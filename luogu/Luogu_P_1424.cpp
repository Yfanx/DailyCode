/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define i64 long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  i64 n, ans = 0;
  int x;
  cin >> x >> n;
  for (int i = 0; i < n; i++) {
    if ((x != 6) && (x != 7)) // 只要星期不等于6和7
      ans += 250;             // 总长度增加250
    if (x == 7)               // 当x等于7的时候
      x = 1;                  // x归位为1
    else                      // 其他情况下（x不等于7的时候）
      x++;                    // x自加1
  }
  cout << ans;
  return 0;
}