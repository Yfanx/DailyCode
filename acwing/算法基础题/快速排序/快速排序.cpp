void quick_sort(int l, int r, vector<int> &a) {
  if (r <= l)
    return;
  int i = l - 1, j = r + 1, mid = (l + r) >> 1;
  int x = a[mid];
  while (i < j) {
    do
      i++;
    while (a[i] < x);
    do
      j--;
    while (a[j] > x);
    if (i < j)
      swap(a[i], a[j]);
  }
  quick_sort(l, j, a);
  quick_sort(j + 1, r, a);
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  vector<int> a(n);
  for (int &x : a)
    cin >> x;
  quick_sort(0, n - 1, a);
  for (int x : a)
    cout << x << ' ';
  cout.tie(0);
  return 0;
}