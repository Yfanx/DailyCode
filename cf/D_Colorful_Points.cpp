/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define i64 long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

const int N = 1000005;
char a[N], ch[N];   // a为输入字符串，ch为每一组的字符
i64 siz[N], x, ans; // siz为每一组的长度，x为组数，ans为操作次数

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    scanf("%s", a + 1);      // 从第一位开始输入
    i64 len = strlen(a + 1); // 同样从第一位开始计算
    for (int i = 1; i <= len; i++) {
        if (a[i] == ch[x])
            siz[x]++; // 满足要求就组数长度+1
        else
            ch[++x] = a[i], siz[x] = 1; // 否则就重新增加一组
    }
    while (x > 1) { // 如果组数>1就一直操作
        for (int i = 1; i <= x; i++) {
            if (i == 1 || i == x)
                siz[i]--; // 如果这是两边的组，就只删除一个数
            else
                siz[i] -= 2; // 如果是中间的，就删除两个数
        }
        i64 cnt = 0;
        for (int i = 1; i <= x; i++) {
            if (siz[i] <= 0) continue; // 如果当前组的长度为0,那么就删除这组（跳过）
            if (ch[i] == ch[cnt])
                siz[cnt] += siz[i]; // 合并组
            else
                ch[++cnt] = ch[i], siz[cnt] = siz[i];
        }
        ans++, x = cnt; // 操作次数+1，调整x为新的组数
    }
    cout << ans << endl; // 输出操作次数
    return 0;
}

