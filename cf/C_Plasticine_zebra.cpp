/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define i64 long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    string s;
    int cnt = 1;
    int ans = 0;
    getline(cin, s);
    s += s;
    int n = s.size();

    for (int i = 1; i < n; i++) {
        if (s[i] != s[i - 1])
            cnt++;
        else
            cnt = 1;
        ans = max(ans, cnt);
    }

    cout << min(ans, n / 2) << endl;

    return 0;
}

