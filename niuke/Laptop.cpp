/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define i64 long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

struct computer {
    int x, y;
};

bool cmp(computer a, computer b) {
    if (a.x == b.x) return a.y < b.y;
    return a.x < b.x;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int n;
    cin >> n;
    vector<computer> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i].x >> a[i].y;
    }
    sort(all(a), cmp);
    int ans = 0;
    for (int i = 0; i < n; i++) {
        for (int j = i + 1; j < n; j++) {
            if (a[j].y > a[i].y) {
                ans++;
                break;
            }
        }
    }
    cout << ans << endl;
    return 0;
}