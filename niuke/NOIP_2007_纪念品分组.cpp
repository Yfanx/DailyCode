/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define i64 long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int n, m;
    cin >> n >> m;
    vector<int> a(m);
    for (int i = 0; i < m; i++) {
        cin >> a[i];
    }
    sort(all(a));
    int ans = 0;
    int sum = 0;
    for (int i = 0; i < m; i++) {
        sum += a[i];
        if (sum <= n) {
            ans++;
        } else {
            break;
        }
    }
    cout << ans << endl;
    return 0;
}
