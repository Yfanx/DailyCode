#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

bool cmp(string a, string b) {
    return a + b > b + a;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int n;
    cin >> n;
    vector<string> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(all(a), cmp);
    for (int i = 0; i < n; i++) {
        cout << a[i];
    }
    return 0;
}