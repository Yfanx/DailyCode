/* #pragma GCC optimize(2) */
#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define i64 long long
#define Debug(x) cout << #x << ':' << x << endl
#define all(x) (x).begin(), (x).end()

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int n, q;
    cin >> n >> q;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    sort(all(a));
    while (q--) {
        int x, y;
        int res = -1;
        cin >> x >> y;
        for (int i = 0; i < n; i++) {
            int temp = pow(10, x);
            if (a[i] % temp == y) {
                res = a[i];
                break;
            }
        }
        cout << res << endl;
    }
    return 0;
}